#include "libft.h"

void	ft_putnbr(int nb)
{
	long int n;

	n = nb;
	if (n < 0)
		ft_putchar('-');
	if (n < 0)
		n = n * -1;
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + 48);
}
