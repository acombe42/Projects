#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int				i;
	unsigned int	j;

	if (!s || !f)
		return ;
	i = 0;
	j = 0;
	while (s[i])
	{
		(f)(j++, &s[i]);
		i++;
	}
}
