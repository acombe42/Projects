#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*a;
	int		i;

	i = 0;
	a = (char*)s;
	while (a[i] != '\0')
		i++;
	while (a[i] != (char)c && i > 0)
		i--;
	if (a[i] == c)
		return (&a[i]);
	return (NULL);
}
