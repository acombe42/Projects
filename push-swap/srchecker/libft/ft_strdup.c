#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	size_t	j;
	char	*dest;

	i = 0;
	j = ft_strlen(s);
	dest = (char*)malloc(sizeof(char) * j + 1);
	if (dest == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = s[i];
	return (dest);
}
